const add = function(num1, num2) {
    return num1 + num2;
}

const subtract = function(num1, num2) {
    return num1 - num2;
}

const multiply = function(num1, num2) {
    return num1 * num2;
}

const divide = function(num1, num2) {
    return num1 / num2;
}

console.log(add(2, 4));
console.log(subtract(4, 2));
console.log(multiply(2, 4));
console.log(divide(4, 2));
